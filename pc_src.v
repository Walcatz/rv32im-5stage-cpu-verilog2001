module pc_src
(
  // =========================================================================
  // 1. 输入总线 (控制单元信号与执行级实际判定结果)
  // =========================================================================
  input  wire        Jump,         // 当前指令是否为 JAL 无条件绝对跳转
  input  wire        Jumpr,        // 当前指令是否为 JALR 寄存器相对跳转
  input  wire        Branch,       // 当前指令是否为 B-Type 条件分支跳转
  input  wire        br_taken,     // ALUResult[0]：1代表满足条件应该跳，0代表不跳
  input  wire        br_predict,   // 随流水线一同顺延过来的、当初取指级给出的预测跳转状态

  // =========================================================================
  // 2. 输出总线 (驱动取指级 PC 主 MUX 以及 Hazard 冲刷网络)
  // =========================================================================
  output wire [1:0]  PCSrc,        // 2位 PC 物理源选择线 (00: PC+4, 01: PCTarget, 10: JALR地址)
  output wire        mispredict    // 预测失败指示信号 (1: 触发流水线冲刷)
);

  // =========================================================================
  // 1. 物理源选择逻辑 (PCSrc)
  //    00 -> 默认常规的 PCF + 4 (若在前级预测跳转，则可能对应预判的目标地址)
  //    01 -> 走向物理计算的 PCTargetE (即 PCE + ImmExtE)
  //    10 -> 走向 ALUResultE 截断最低位后的物理地址 (专门服务 JALR)
  // =========================================================================
  assign PCSrc = (Jump || (Branch && br_taken)) ? 2'b01 :
                 (Jumpr)                        ? 2'b10 :
                                                  2'b00;

  // =========================================================================
  // 2. 预测失败校检核心逻辑 (mispredict)
  //    情况 A: 无条件跳转指令 (JAL/JALR) 执行时，如果取指阶段没预测到跳转 (!br_predict)，判定失败
  //    情况 B: 条件分支指令 (Branch) 执行时，如果最终的实际跳转动作 (br_taken) 
  //            与前级的预测偏置 (br_predict) 不对等，判定失败
  // =========================================================================
  assign mispredict = ((Jump || Jumpr) && !br_predict) || 
                      (Branch && (br_taken != br_predict));

endmodule